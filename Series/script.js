const wrapper = document.querySelector('.wrapper');
const seasonsDiv = document.getElementById('seasons-container');
const episodesDiv = document.getElementById('episodes-container');
const btnPopup = document.querySelector('.btndisplay-popup');
const iconClose = document.querySelector('.icon-close');

const params = new URLSearchParams(window.location.search);
const mediaName = params.get('name');
let currentSeasonIndex = parseInt(params.get('season')) || 0;

function loadMediaData() {
    if (mediaName && database[mediaName]) {
        const series = database[mediaName];

        const currentSeason = series.seasons[currentSeasonIndex];

        document.getElementById('media-title').innerText = mediaName;
        document.getElementById('media-poster').src = currentSeason.poster;
        document.getElementById('media-description').innerText = series.description;

        document.getElementById('trailer-container').innerHTML = `
            <iframe src="https://www.youtube.com/embed/${currentSeason.trailerID}" frameborder="0" allowfullscreen></iframe>
        `;

        seasonsDiv.innerHTML = "";
        series.seasons.forEach((s, index) => {
            const card = document.createElement('div');
            card.className = `season-card ${index === currentSeasonIndex ? 'active' : ''}`;
            card.innerText = s.name;

            card.onclick = () => {
                window.location.href = `?name=${encodeURIComponent(mediaName)}&season=${index}`;
            };
            seasonsDiv.appendChild(card);
        });

        // تفريغ قائمة الحلقات
        episodesDiv.innerHTML = "";

        currentSeason.episodes.forEach(ep => {
            const a = document.createElement('a');
            a.className = 'episode-item';
            a.href = "javascript:void(0)"; // منع الرابط من فتح صفحة جديدة
            a.innerText = ep.title;

            // عند الضغط على الحلقة
            a.onclick = () => {
                const videoPlayer = document.getElementById('main-video-player');
                const playingTitle = document.getElementById('now-playing');

                // تحديث عنوان الحلقة المشغلة
                playingTitle.innerText = `مشاهدة الآن: ${mediaName} - ${currentSeason.name} - ${ep.title}`;

                // تغيير مسار الفيديو وتشغيله
                videoPlayer.src = ep.path;
                videoPlayer.play();

                // تمرير الصفحة تلقائياً للمشغل (Optional)
                videoPlayer.scrollIntoView({ behavior: 'smooth' });
            };

            episodesDiv.appendChild(a);
        });

        setTimeout(() => wrapper.classList.add('active-popup'), 100);
    } else {
        console.error("المسلسل غير موجود في قاعدة البيانات!");
        document.getElementById('media-title').innerText = "Not Found";
    }
}

btnPopup.addEventListener('click', () => wrapper.classList.add('active-popup'));
iconClose.addEventListener('click', () => wrapper.classList.remove('active-popup'));

loadMediaData();